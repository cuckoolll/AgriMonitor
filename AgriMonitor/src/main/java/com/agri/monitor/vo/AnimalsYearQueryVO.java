public class AnimalsYearQueryVO extends CommonQueryVO {
	private String county;
	private String agri_address;
	private String date_year;
	private String date_year1;

	public String getCounty() {
		return county;
	}
	public void setCounty(String county) {
		this.county = county;
	}
	public String getAgri_address() {
		return agri_address;
	}
	public void setAgri_address(String agri_address) {
		this.agri_address = agri_address;
	}
	public String getDate_year() {
		return date_year;
	}
	public void setDate_year(String date_year) {
		this.date_year = date_year;
	}
	public String getDate_year1() {
		return date_year1;
	}
	public void setDate_year1(String date_year1) {
		this.date_year1 = date_year1;
	}
	
}
